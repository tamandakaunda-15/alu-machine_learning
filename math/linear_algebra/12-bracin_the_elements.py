#!/usr/bin/env python3
def np_elementwise(mat1, mat2):
    """
    Performs element-wise addition, subtraction, multiplication, and division
    of two matrices (or a matrix and a scalar).

    Args:
        mat1: The first input matrix (numpy-compatible array or scalar).
        mat2: The second input matrix or scalar.

    Returns:
        tuple: A tuple containing the element-wise sum, difference, product, and quotient.
    """
    add = mat1 + mat2
    sub = mat1 - mat2
    mul = mat1 * mat2
    div = mat1 / mat2
    
    return add, sub, mul, div                                                                                   
